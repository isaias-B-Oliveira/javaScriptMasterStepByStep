// Os tipos de dados em JavaScript são divididos em dois grupos: Primitivos e Objetos.
// No monento famos abordar os primitivos

// string
// sao textos que podem ser escritos entre aspas simples ou duplas
"Lucas";
console.log("Lucas"); // Lucas

// number
// sao numeros inteiros ou decimais
10;
10.5;
console.log(10); // 10
console.log(10.5); // 10.5

// boolean
// sao valores que podem ser verdadeiro ou falso
true;
false;
console.log(true); // true
console.log(false); // false

// Os tipos primitivos em JavaScript são os tipos básicos de dados que não são objetos e representam valores imutáveis. Isso significa que, quando você manipula um valor primitivo, na verdade está trabalhando com uma cópia do valor, e não com a referência dele na memória.
